const express = require("express");
const cors = require("cors");

const app = express();
const PORT = process.env.AI_PORT || 3001;

app.use(cors());
app.use(express.json());

app.post("/api/generate-ai", async (req, res) => {
  const { action, text, title } = req.body;

  let prompt = "";

  if (action === "improve") {
    prompt = `
      Ты — профессиональный редактор объявлений и специалист по модерации текста.
      Твоя задача: сделать описание привлекательным, вежливым и полностью очищенным от токсичности.

      СТРОГИЕ ПРАВИЛА ЦЕНЗУРЫ:
      1. ПОЛНОСТЬЮ УДАЛИ или замени на культурные аналоги любой мат, нецензурную лексику, оскорбления и грубый жаргон.
      2. Если в тексте есть агрессия — перепиши её в спокойном, деловом тоне.
      3. Описание должно соответствовать правилам приличия и законам РФ (никакой дискриминации или запрещенных тем).
      4. Ответ пиши обязательно на русском языке

      ФОРМАТ ОТВЕТА (Пиши СТРОГО по этой структуре):
      Название товара: [Подходящее название]

      Описание: 
      [Твой текст сплошным абзацем. НИКАКИХ списков, НИКАКОЙ нецензурщины. Пиши грамотно и чисто.]

      Преимущества:
      - [Пункт 1]
      - [Пункт 2]

      ОТРЕДАКТИРУЙ ЭТОТ ТЕКСТ: 
      "${text}"

      Важно: Начни ответ сразу с "Название товара:". Не пиши "Вот ваш текст" или "Я исправил".
    `;
  } else if (action === "price") {
    prompt = `
      Проанализируй товар: "${title}".
      Выдай ответ строго по шаблону:
      Название товара: ${title}
      Рекомендуемая цена: [число] рублей (обязательно без пробелов вне зависимости от длины числа)
      Обоснование: [одно короткое предложение]
      Ответ пиши обязательно на русском языке
      
      Не пиши ничего, кроме этих трех строк.
    `;
  } else if (action === "chat") {
    const { history, adContext } = req.body;

    // Формируем системный контекст для модели
    const systemContext = `
Ты — эксперт-консультант торговой площадки. Твоя задача — помогать владельцу объявления максимально эффективно продать товар.

ДАННЫЕ ОБЪЯВЛЕНИЯ:
- Название: ${adContext.title}
- Категория: ${
      adContext.category === "electronics"
        ? "Электроника"
        : adContext.category === "auto"
          ? "Автомобили"
          : adContext.category === "real_estate"
            ? "Недвижимость"
            : adContext.category
    }
- Цена: ${adContext.price} руб.
- Описание: ${adContext.description}
- Дополнительные параметры: ${Object.entries(adContext.params || {})
      .map(([key, val]) => `${key}: ${val}`)
      .join(", ")}

ИНФОРМАЦИЯ О ДОПУСТИМЫХ ПАРАМЕТРАХ:
1. Для автомобилей: марка, модель, год выпуска, тип трансмиссии (автомат или механика), пробег, мощность двигателя.
2. Для недвижимости: тип (квартира, дом, комната), адрес, площадь, этаж.
3. Для электроники: тип устройства (телефон, ноутбук и др.), марка, модель, состояние (новый или б/у), цвет.

ТВОИ ПРАВИЛА:
1. Отвечай только на русском языке.
2. Пиши ответ сплошным текстом, без нумерованных или маркированных списков.
3. Будь кратким, говори строго по делу.
4. Давай практические советы: как улучшить текст, адекватна ли цена, какие характеристики стоит добавить, чтобы привлечь покупателей.
5. Если в параметрах указаны технические значения (например, 'automatic', 'flat', 'used'), в ответе используй их русские эквиваленты: 'автомат', 'квартира', 'б/у'.
6. Запомни что других типов кроме тех что я тебе передал не существует. Если нет ничего похожего то говори что платформа находится на этапе разрабоки и категория для вашего товара появится похже
`;

    // Склеиваем историю сообщений для Ollama
    // В идеале использовать Chat API Ollama, но для простоты добавим в prompt
    const chatPrompt = `${systemContext}\n\nИстория чата:\n${history.map((m) => `${m.role}: ${m.content}`).join("\n")}\nassistant:`;

    prompt = chatPrompt;
  }

  try {
    const ollamaResponse = await fetch("http://127.0.0.1:11434/api/generate", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        model: "llama3",
        prompt: prompt,
        stream: false,
        options: {
          temperature: 0.2, // Низкая температура для стабильного формата
        },
      }),
    });

    if (!ollamaResponse.ok) {
      const errorText = await ollamaResponse.text();
      throw new Error(`Ollama error: ${errorText}`);
    }

    const data = await ollamaResponse.json();
    // Возвращаем результат фронтенду
    res.json({ result: data.response });
  } catch (error) {
    console.error("AI Error:", error);
    res.status(500).json({ error: "Ошибка генерации. Проверь работу Ollama." });
  }
});

app.listen(PORT, () => {
  console.log(`AI Сервис запущен на http://localhost:${PORT}`);
});
